package com.ycl.sportsing.domain;

public class DongTai {
	private int dongtai_id;
	private String publisher_id;
	private String dongtai_details;
	private String dongtai_picture;
	private String dongtai_time;

	public int getDongtai_id() {
		return dongtai_id;
	}

	public void setDongtai_id(int dongtai_id) {
		this.dongtai_id = dongtai_id;
	}

	public String getPublisher_id() {
		return publisher_id;
	}

	public void setPublisher_id(String publisher_id) {
		this.publisher_id = publisher_id;
	}

	public String getDongtai_details() {
		return dongtai_details;
	}

	public void setDongtai_details(String dongtai_details) {
		this.dongtai_details = dongtai_details;
	}

	public String getDongtai_picture() {
		return dongtai_picture;
	}

	public void setDongtai_picture(String dongtai_picture) {
		this.dongtai_picture = dongtai_picture;
	}

	public String getDongtai_time() {
		return dongtai_time;
	}

	public void setDongtai_time(String dongtai_time) {
		this.dongtai_time = dongtai_time;
	}
}




