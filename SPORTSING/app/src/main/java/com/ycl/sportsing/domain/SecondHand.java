package com.ycl.sportsing.domain;

public class SecondHand {
	private int secondhand_id;
	private String publisher_id;
	private String secondhand_title;
	private String secondhand_details;
	private String secondhand_price;
	private String secondhand_picture;

	public int getSecondhand_id() {
		return secondhand_id;
	}

	public void setSecondhand_id(int secondhand_id) {
		this.secondhand_id = secondhand_id;
	}

	public String getPublisher_id() {
		return publisher_id;
	}

	public void setPublisher_id(String publisher_id) {
		this.publisher_id = publisher_id;
	}

	public String getSecondhand_title() {
		return secondhand_title;
	}

	public void setSecondhand_title(String secondhand_title) {
		this.secondhand_title = secondhand_title;
	}

	public String getSecondhand_details() {
		return secondhand_details;
	}

	public void setSecondhand_details(String secondhand_details) {
		this.secondhand_details = secondhand_details;
	}

	public String getSecondhand_price() {
		return secondhand_price;
	}

	public void setSecondhand_price(String secondhand_price) {
		this.secondhand_price = secondhand_price;
	}

	public String getSecondhand_picture() {
		return secondhand_picture;
	}

	public void setSecondhand_picture(String secondhand_picture) {
		this.secondhand_picture = secondhand_picture;
	}
}




